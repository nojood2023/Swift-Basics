import Foundation

class Utils {

    static func readInt() -> Int {
        return Int(readLine()!)!
    }
    
    static func readFloat() -> Float {
        return Float(readLine()!)!
    }
    
    static func readDouble() -> Double {
        return Double(readLine()!)!
    }
    
    static func readString() -> String {
        return readLine()!
    }

}
